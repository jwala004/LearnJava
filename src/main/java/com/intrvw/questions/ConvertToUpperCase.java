package com.intrvw.questions;

public class ConvertToUpperCase {

	private static String str = "jwala";

	public static void main(String[] args) {
		
		System.out.println("Original string before converting to upper case: "+str);
		
		String strUpperCase = str.toUpperCase();
		
		System.out.println("String after converting to upper case: "+strUpperCase);
		
		
	}

}
