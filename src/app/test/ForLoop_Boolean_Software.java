package app.test;

public class ForLoop_Boolean_Software {

	public static void main(String[] args) {
		
		for (int i = 0; ; i++) {
			
			//by default the condition in for loop is considered as true 
			//(if u r not assigning any condition then it will be considered as true!)
			
		System.out.println(i);
			
		}
		
		
	//System.out.println("hi");
		
	}

}
