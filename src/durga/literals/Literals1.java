package durga.literals;

public class Literals1 {
	public static void main(String[] args) {
		int x=10;
		int y1=010;//0*8 to power 0 + 1*8 to power 1= ( 0+8=8)
		int y2=0110;//0*8 to power 0 + 1*8 to power + 1*8 to power 2= 
		int y3=01110;
		int z=0X10;
		//System.out.println(8*8*8+8*8+8);
		System.out.println(x+" "+y1+" "+y2+" "+y3+" "+z); 
	}

}
